    public MostrarLibros(Menu menu, PilaLibros pilaL) {
        initComponents();
        principal = menu;
        pila = pilaL;
        Libros arregloNodos[] = new Libros [pila.getCantidadN()];
        DefaultTableModel modelo = new DefaultTableModel();
        String[] cabecera = {"Nombre", "Editorial", "AÃ±o", "Autor"};
        modelo.setColumnIdentifiers(cabecera);
        Object[] datos = new Object[pila.getCantidadN()];
        for(int i = 0; i<pila.getCantidadN(); i++){
            datos[0] = i+1;
            datos[1] =  arregloNodos[i].getNombre();
            datos[2] = arregloNodos[i].getEditorial();
            datos[3] = arregloNodos[i].getYear();
            datos[4] = arregloNodos[i].getAutor();
            modelo.addRow(datos);
        }
        jTable2.setModel(modelo);
    }

